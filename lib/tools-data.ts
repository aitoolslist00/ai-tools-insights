export function getToolById(id: string): AITool | undefined {
  return aiToolsData.find(tool => tool.id === id)
}

export function getToolsByCategory(category: string): AITool[] {
  return aiToolsData.filter(tool => tool.category === category)
}

export function getAllCategories(): string[] {
  const categories = new Set(aiToolsData.map(tool => tool.category))
  return Array.from(categories).sort()
}

export function searchTools(query: string): AITool[] {
  const lowercaseQuery = query.toLowerCase()
  return aiToolsData.filter(tool =>
    tool.name.toLowerCase().includes(lowercaseQuery) ||
    tool.description.toLowerCase().includes(lowercaseQuery) ||
    tool.category.toLowerCase().includes(lowercaseQuery) ||
    tool.features.some(feature => feature.toLowerCase().includes(lowercaseQuery)) ||
    tool.useCases.some(useCase => useCase.toLowerCase().includes(lowercaseQuery))
  )
}

export function getFeaturedTools(limit: number = 6): AITool[] {
  return aiToolsData
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit)
}

export function getToolsByRating(minRating: number = 4.0): AITool[] {
  return aiToolsData
    .filter(tool => tool.rating >= minRating)
    .sort((a, b) => b.rating - a.rating)
}

export function getToolAlternatives(toolId: string): AITool[] {
  const tool = getToolById(toolId)
  if (!tool || !tool.alternatives) return []
  
  return tool.alternatives
    .map(altId => getToolById(altId))
    .filter((altTool): altTool is AITool => altTool !== undefined)
}
